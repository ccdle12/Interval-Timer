public class RoundsModel
{
    private String workMins;
    private String workSecs;
    private String restMins;
    private String restSecs;
    private String rounds;

    public String getWorkMins() {
        return workMins;
    }

    public void setWorkMins(String workMins) {
        this.workMins = workMins;
    }

    public String getWorkSecs() {
        return workSecs;
    }

    public void setWorkSecs(String workSecs) {
        this.workSecs = workSecs;
    }

    public String getRestMins() {
        return restMins;
    }

    public void setRestMins(String restMins) {
        this.restMins = restMins;
    }

    public String getRestSecs() {
        return restSecs;
    }

    public void setRestSecs(String restSecs) {
        this.restSecs = restSecs;
    }

    public String getRounds() {
        return rounds;
    }

    public void setRounds(String rounds) {
        this.rounds = rounds;
    }
}
